# for ციკლის მეშვეობით შეასრულეთ ყველა მათემატიკური ოპერაცია 0-დან 100-მდე რიცხვებზე
sum_all = 0
product_all = 1
difference_all = 0
division_result = 0

for num in range(1, 101):  # ვიწყებთ 1-დან, რადგან 0-ზე გაყოფა არასწორია
    sum_all += num             # შეკრება
    product_all *= num         # გამრავლება
    difference_all -= num      # გამოკლება
    division_result += 1 / num  # გაყოფა

print("შეკრება:", sum_all)
print("გამრავლება:", product_all)
print("გამოკლება:", difference_all)
print("გაყოფა:", division_result)

#მომხმარებელს შემოატანინეთ რიცხვი და შემდეგ for loop - ის გამოყენებით გამოიტანეთ ამ რიცხვამდე ყველა რიცხვის ჯამი(მაგალითად თუ შეიყვანს 10ს გამოიტანეთ 10მდე ყველა რიცხვის ჯამი მაგალითად).
# მომხმარებლისგან რიცხვის შემოტანა
number = int(input("შეიყვანეთ რიცხვი: "))

# ჯამის ცვლადი
sum_all = 0

# for loop 1-დან შეყვანილ რიცხვამდე
for num in range(1, number + 1):
    sum_all += num  # რიცხვების ჯამი

# შედეგის დაბეჭდვა
print("რიცხვი " + str(number) + "-მდე რიცხვების ჯამი არის: " + str(sum_all))

#მომხმარებელს შემოატანინეთ ორი რიცხვი ხოლო ამის შემდეგ for loop - ის გამოყენებით გამოიტანეთ ამ რიცხვებს შორის ჯამი და ნამრავლი.
# მომხმარებლისგან ორი რიცხვის შემოტანა
num1 = int(input("შეიყვანეთ პირველი რიცხვი: "))
num2 = int(input("შეიყვანეთ მეორე რიცხვი: "))

# დავრწმუნდეთ რომ num1 ნაკლებია num2-ზე
if num1 > num2:
    num1, num2 = num2, num1  # რიცხვების გაცვლა, თუ num1 > num2

# ჯამი და ნამრავლი
sum_all = 0
product_all = 1

# for loop num1-დან num2-მდე
for num in range(num1, num2 + 1):
    sum_all += num       # რიცხვების ჯამი
    product_all *= num    # რიცხვების ნამრავლი

# შედეგების დაბეჭდვა
print("რიცხვების ჯამი არის: " + str(sum_all))
print("რიცხვების ნამრავლი არის: " + str(product_all))

#დაწერეთ ალგორითმი, რომელიც მომხმარებელს შეეკითხება რიცხვს. თუ რიცხვი ლუწია გაყავით ორზე, სხვა შემთხვევაში გაამრავლეთ სამზე და მიუმატეთ ერთი.
# მომხმარებლისგან რიცხვის შემოტანა
number = int(input("შეიყვანეთ რიცხვი: "))

# ლუწობის ან კენტობის შემოწმება
if number % 2 == 0:
    result = number // 2  # თუ ლუწია, გაყოფა 2-ზე
else:
    result = number * 3 + 1  # თუ კენტია, გამრავლება 3-ზე და +1

# შედეგის დაბეჭდვა
print("შედეგი არის: " + str(result))

## საწყისი რიცხვი
num = 1

